/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick.
   */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */
  uint32_t counter = 0;
  uint32_t last_tick = 0;
  /* USER CODE END 2 */

  while (1) {
    if (usb_cmd_ready) {
      usb_cmd_ready = 0;

      // Print welcome banner on first interaction
      if (!session_started) {
        session_started = 1;
        const char *banner = "\r\n"
                             "========================================\r\n"
                             "   STM32 USB Serial Terminal v1.0\r\n"
                             "========================================\r\n"
                             "Commands: start, stop, status, exit\r\n"
                             "Press Ctrl+C to stop counter\r\n"
                             "========================================\r\n"
                             "\r\n";
        CDC_Send_Wait(banner, strlen(banner));
        HAL_Delay(5);
      }

      // 1. Check for Empty Command (User just pressed Enter)
      if (strlen(usb_cmd) == 0) {
        // User just hit Enter. ISR already printed \r\n.
        // We just print the prompt again.
        CDC_Transmit_FS((uint8_t *)"> ", 2);
        continue;
      }

      if (strcmp(usb_cmd, "start") == 0) {
        counter_enabled = 1;
        CDC_Transmit_FS((uint8_t *)"Counter started\r\n", 17);
      } else if (strcmp(usb_cmd, "stop") == 0) {
        counter_enabled = 0;
        CDC_Transmit_FS((uint8_t *)"Counter stopped\r\n", 17);
      } else if (strcmp(usb_cmd, "exit") == 0) {
        counter_enabled = 0;
        CDC_Transmit_FS((uint8_t *)"Exited loop mode\r\n", 18);
      } else if (strcmp(usb_cmd, "status") == 0) {
        char msg[50];
        sprintf(msg, "Counter=%lu, Enabled=%d\r\n", counter, counter_enabled);
        CDC_Transmit_FS((uint8_t *)msg, strlen(msg));
      } else {
        CDC_Transmit_FS((uint8_t *)"Unknown command\r\n", 17);
      }

      // Clean buffer and reprint prompt
      memset(usb_cmd, 0, sizeof(usb_cmd));

      // Wait a tiny bit to ensure previous message sent (simple flow control)
      HAL_Delay(2);

      // Print the prompt for the next line
      CDC_Transmit_FS((uint8_t *)"> ", 2);
    }

    /* ---- Non-blocking counter ---- */
    if (counter_enabled && (HAL_GetTick() - last_tick >= 1000)) {
      last_tick = HAL_GetTick();
      counter++;

      // Clear current line and print counter cleanly (no prompt)
      char msg[64];
      sprintf(msg, "\rCounter: %lu                    \r\n", counter);
      CDC_Send_Wait(msg, strlen(msg));
    }
  }
  /* USER CODE END 3 */
}

